package statementSwitch;

public class Switch2 {

	public static void main(String[] args) 
	{
	 String mobile="iPhone";
	 switch (mobile)
	 {
		 case"samsung":System.out.println("Buy a samsung phone");
		 break;
		 case"iPhone":System.out.println("Buy a iPhone");
		 break;
		 case"Redmi":System.out.println("Buy a Redmi phone");
		 break;
		 default:System.out.println("In default");
		 
	 }

	}

}
